package exercicios;

public class FuncoesPuras {

	/*
	 * Funções puras são funções que não têm efeitos colaterais e 
	 * sempre produzem o mesmo resultado para os mesmos argumentos. 
	 * Em outras palavras, uma função pura não altera nenhum estado externo 
	 * e não depende de nenhum estado externo.
	 */
	
	public static int soma(int a, int b) {
        return a + b;
    }
	
}
